def _check_verdict(actual_str: str | None, expected_str: str | None) -> Verdict:
    """Safely compare the JSON output generated by the driver with the expected output."""
    if not actual_str:
        return Verdict.WA
        
    if actual_str.startswith("Runtime Error"):
        return Verdict.RE
        
    try:
        # Compare as parsed JSON objects to ignore formatting/spacing differences
        return Verdict.ACC if json.loads(actual_str) == json.loads(expected_str) else Verdict.WA
    except (json.JSONDecodeError, TypeError):
        # Fallback to strict string matching if parsing fails
        return Verdict.ACC if str(actual_str).strip() == str(expected_str).strip() else Verdict.WA


def _finalise(
    job_id: str,
    verdict: Verdict,
    exec_time_ms: int,
    memory_mb: int,
    stdout_snippet: str,
    stderr_snippet: str,
    passed_cases: int = 0,
    total_cases: int = 0,
    failed_tc_id: Any | None = None,
    actual: str | None = None,
    expected: str | None = None,
) -> None:
    """Write final result to PostgreSQL and publish to Redis Pub/Sub."""
    clean_stdout = stdout_snippet.strip()[:1000] if stdout_snippet else None
    clean_stderr = stderr_snippet.strip()[:512] if stderr_snippet else None

    with get_sync_db() as db:
        db.execute(
            text("""
                UPDATE submissions
                SET status = 'completed',
                    verdict = :verdict,
                    execution_time_ms = :exec_time_ms,
                    memory_used_mb = :memory_mb,
                    stdout_snippet = :stdout_snippet,
                    stderr_snippet = :stderr_snippet,
                    passed_test_cases = :passed,
                    total_test_cases = :total,
                    failed_test_case_id = :failed_id,
                    actual_output = :actual,
                    expected_output = :expected
                WHERE id = :job_id
            """),
            {
                "verdict": verdict.value,
                "exec_time_ms": exec_time_ms,
                "memory_mb": memory_mb,
                "stdout_snippet": clean_stdout,
                "stderr_snippet": clean_stderr,
                "passed": passed_cases,
                "total": total_cases,
                "failed_id": failed_tc_id,
                "actual": actual,
                "expected": expected,
                "job_id": job_id,
            },
        )

    payload = {
        "job_id": job_id,
        "status": "completed",
        "verdict": verdict.value,
        "execution_time_ms": exec_time_ms,
        "memory_used_mb": memory_mb,
        "stdout_snippet": clean_stdout,
        "stderr_snippet": clean_stderr,
        "passed_test_cases": passed_cases,
        "total_test_cases": total_cases,
        "failed_test_case_id": str(failed_tc_id) if failed_tc_id else None,
        "actual_output": actual,
        "expected_output": expected,
    }
    _redis.publish(f"job_updates:{job_id}", json.dumps(payload))


